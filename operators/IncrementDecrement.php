		//Pre-Increment//

		$x = 10; 

		echo ++$x;

		echo "<br>";

		//Post-Increment//

		$x = 10; 

		echo $x++;

		echo "<br>";

		//Pre-Decrement//

		$x = 10; 

		echo --$x;

		echo "<br>";

		//Post-Decrement//

		$x = 10; 

		echo $x--;

		echo "<br>";



	?>
	
